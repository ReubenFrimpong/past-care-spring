-- SMS Messages Table (Message Tracking)
CREATE TABLE sms_messages (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    sender_id BIGINT NOT NULL,
    church_id BIGINT NOT NULL,
    member_id BIGINT,
    recipient_phone VARCHAR(20) NOT NULL,
    recipient_name VARCHAR(200),
    message TEXT NOT NULL,
    message_count INT NOT NULL DEFAULT 1,
    cost DECIMAL(10, 2) NOT NULL,
    status VARCHAR(20) NOT NULL,
    gateway_message_id VARCHAR(100),
    gateway_response TEXT,
    delivery_status VARCHAR(50),
    delivery_time DATETIME,
    scheduled_time DATETIME,
    sent_at DATETIME,
    created_at DATETIME NOT NULL,
    updated_at DATETIME,
    FOREIGN KEY (sender_id) REFERENCES users(id) ON DELETE CASCADE,
    FOREIGN KEY (church_id) REFERENCES churches(id) ON DELETE CASCADE,
    FOREIGN KEY (member_id) REFERENCES members(id) ON DELETE SET NULL,
    INDEX idx_sender_created (sender_id, created_at),
    INDEX idx_church_created (church_id, created_at),
    INDEX idx_status (status),
    INDEX idx_scheduled (scheduled_time, status),
    INDEX idx_gateway_message (gateway_message_id),
    INDEX idx_recipient_phone (recipient_phone)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
