package com.reuben.pastcare_spring.enums;

/**
 * Dashboard widget categories for organizing widgets by functional area.
 * Used in Dashboard Phase 2 customization features.
 */
public enum WidgetCategory {
    /**
     * Statistical overview widgets (e.g., key metrics, summary cards)
     */
    STATS,

    /**
     * Pastoral care widgets (e.g., care needs, birthdays, prayer requests)
     */
    PASTORAL_CARE,

    /**
     * Analytics and reporting widgets (e.g., growth charts, attendance trends)
     */
    ANALYTICS,

    /**
     * Operational widgets (e.g., events, donations, SMS credits)
     */
    OPERATIONS
}
