package com.reuben.pastcare_spring.enums;

/**
 * Age group classifications for visitors and members.
 * Used for demographic analysis and targeted programs.
 *
 * Phase 1: Enhanced Attendance Tracking
 */
public enum AgeGroup {
    /**
     * Children (0-12 years).
     */
    CHILD,

    /**
     * Teenagers (13-19 years).
     */
    TEEN,

    /**
     * Young adults (20-35 years).
     */
    YOUNG_ADULT,

    /**
     * Adults (36-59 years).
     */
    ADULT,

    /**
     * Seniors (60+ years).
     */
    SENIOR
}
