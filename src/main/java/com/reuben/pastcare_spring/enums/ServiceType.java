package com.reuben.pastcare_spring.enums;

/**
 * Types of church services and events for attendance tracking.
 *
 * Phase 1: Enhanced Attendance Tracking
 */
public enum ServiceType {
    /**
     * Main Sunday worship service (usually morning).
     */
    SUNDAY_MAIN_SERVICE,

    /**
     * Second Sunday service (afternoon/evening).
     */
    SUNDAY_SECOND_SERVICE,

    /**
     * Midweek service (Wednesday/Thursday).
     */
    MIDWEEK_SERVICE,

    /**
     * Prayer meeting or prayer vigil.
     */
    PRAYER_MEETING,

    /**
     * Bible study session.
     */
    BIBLE_STUDY,

    /**
     * Youth service or youth program.
     */
    YOUTH_SERVICE,

    /**
     * Special events (e.g., anniversaries, dedications).
     */
    SPECIAL_EVENT,

    /**
     * Conference or seminar.
     */
    CONFERENCE,

    /**
     * Retreat or camp.
     */
    RETREAT,

    /**
     * Outreach or evangelism event.
     */
    OUTREACH,

    /**
     * Fellowship meeting (small groups).
     */
    FELLOWSHIP_MEETING,

    /**
     * Choir practice or music rehearsal.
     */
    CHOIR_PRACTICE,

    /**
     * Other service types not categorized above.
     */
    OTHER
}
