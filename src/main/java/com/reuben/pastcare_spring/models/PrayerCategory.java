/**
 * Categories for prayer requests
 */
public enum PrayerCategory {
    HEALING,              // Physical healing
    GUIDANCE,             // Seeking God's direction
    PROVISION,            // Financial/material needs
    PROTECTION,           // Safety and protection
    SALVATION,            // Salvation of loved ones
    RELATIONSHIPS,        // Family/marriage/friendships
    GRIEF,                // Loss and bereavement
    ADDICTION,            // Deliverance from addiction
    MENTAL_HEALTH,        // Anxiety, depression, etc.
    EMPLOYMENT,           // Job-related requests
    MINISTRY,             // Church/ministry needs
    TRAVEL,               // Safe travels
    EXAMS,                // Academic success
    PREGNANCY,            // Pregnancy and childbirth
    BREAKTHROUGH,         // Spiritual breakthrough
    THANKSGIVING,         // Praise and thanksgiving
    OTHER                 // Other requests
}
